          >
            <n-select
              v-model:value="backgroundPresetId"
              :options="backgroundPresetOptions"
              size="small"
            />
          </n-form-item-gi>
          <n-form-item-gi
            v-if="backgroundType === 'solid'"
            :label="t('backgroundColor')"
            :show-feedback="false"
          >
            <n-color-picker v-model:value="backgroundColor" :modes="['hex']" size="small" />
          </n-form-item-gi>
          <n-form-item-gi :label="t('windowStyle')" :show-feedback="false">
            <n-select v-model:value="windowStyle" :options="windowStyleOptions" size="small" />
          </n-form-item-gi>
          <n-form-item-gi :label="t('lineNumbers')" :show-feedback="false">
            <n-switch v-model:value="showLineNumbers" />
          </n-form-item-gi>
        </n-grid>
      </ToolSection>

      <ToolSectionHeader>{{ t('layoutTitle') }}</ToolSectionHeader>
      <ToolSection>
        <n-grid cols="1 s:2" responsive="screen" :x-gap="12" :y-gap="12">
