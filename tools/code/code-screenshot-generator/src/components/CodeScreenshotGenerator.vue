        <n-form-item :label="t('codeInputLabel')" :show-feedback="false">
          <n-input
            v-model:value="code"
            type="textarea"
            class="code-input"
            :autosize="{ minRows: 12, maxRows: 28 }"
            :placeholder="t('codePlaceholder')"
          />
        </n-form-item>
      </ToolSection>

      <ToolSectionHeader>{{ t('styleTitle') }}</ToolSectionHeader>
      <ToolSection>
        <n-grid cols="1 s:2" responsive="screen" :x-gap="12" :y-gap="12">
          <n-form-item-gi :label="t('language')" :show-feedback="false">
            <n-select v-model:value="language" :options="languageSelectOptions" size="small" />
          </n-form-item-gi>
          <n-form-item-gi :label="t('renderMode')" :show-feedback="false">
            <n-select v-model:value="renderMode" :options="renderModeOptions" size="small" />
          </n-form-item-gi>
          <n-form-item-gi :label="t('theme')" :show-feedback="false">
            <n-select v-model:value="themeId" :options="themeOptions" size="small" />
          </n-form-item-gi>
          <n-form-item-gi :label="t('backgroundType')" :show-feedback="false">
            <n-select
              v-model:value="backgroundType"
              :options="backgroundTypeOptions"
              size="small"
            />
          </n-form-item-gi>
          <n-form-item-gi
            v-if="backgroundType === 'preset'"
            :label="t('backgroundPreset')"
